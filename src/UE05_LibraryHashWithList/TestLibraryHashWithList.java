    @Test
    public void testFindBookOnRightIndex()
    {
        Book book = new Book(1001450015,"Alice im Wunderland");
        int calcIndex = book.getId() % size;
        LinkedList<Book> foundLinkedList = hashTable.getLinkedListAtPos(calcIndex);
        Iterator it = foundLinkedList.iterator();
        boolean found = false;
        while (it.hasNext() && found == false)
        {
            if (((Book)it.next()).getTitle().equals("Alice im Wunderland") == true)
                found = true;
        }

        assertEquals(true, found);
    }

